public class BitWise_Operator {

    // And
    public static void main(String args[]) {

        System.out.println(5 & 6);

        // Or
        System.out.println(5 | 6);

        // Xor
        System.out.println(5 ^ 6);

        // One's Compliment
        System.out.println(~5);

        // left shift
        System.out.println(5 << 2);

        // Right shift
        System.out.println(6 >> 1);

    }
}